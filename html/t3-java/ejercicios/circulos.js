/*
Este fichero contiene el código
para ocultar un círculo
*/
function ocultar(id) {
    // Esta función permite ocultar un círculo
    document.getElementById(id).style.visibility = 'hidden';
    document.getElementById(id).style.visibility = 'hidden';
    document.getElementById(id).style.visibility = 'hidden';
}

function mostrar(id) {
    // Esta función permite mostrar un círculo
    document.getElementById(id).style.visibility = 'visible';
    document.getElementById(id).style.visibility = 'visible';
    document.getElementById(id).style.visibility = 'visible';
}